package com.minsait.onesait.platform.reports.exception;

public class UploadFileException extends ReportException {

	private static final long serialVersionUID = 1899791553378752928L;

	public UploadFileException() {
		super();
	}

	public UploadFileException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public UploadFileException(String message, Throwable cause) {
		super(message, cause);
	}

	public UploadFileException(String message) {
		super(message);
	}

	public UploadFileException(Throwable cause) {
		super(cause);
	}
}
